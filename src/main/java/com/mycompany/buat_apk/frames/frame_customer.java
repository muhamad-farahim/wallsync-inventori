    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        String name = nameField.getText().trim();
        String phone = phoneField.getText().trim();
        String subdistrict = domisiliField.getText().trim();
        String dobString = dobField.getText().trim(); 

        if (name.isEmpty() || phone.isEmpty() || subdistrict.isEmpty() || dobString.contains("_") || dobString.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Semua kolom (Nama, No. Telepon, Domisili, dan Tanggal Lahir) harus diisi dengan lengkap!", 
                    "Validasi Gagal", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        java.util.Date dobDate = null;
        java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("dd-MM-yyyy");
        dateFormat.setLenient(false); 

        try {
            dobDate = dateFormat.parse(dobString);
        } catch (java.text.ParseException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Format tanggal lahir tidak valid! Gunakan susunan DD-MM-YYYY.", 
                    "Validasi Gagal", 
                    javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        com.mycompany.buat_apk.domains.entities.customers.CreateCustomer customerData = 
                new com.mycompany.buat_apk.domains.entities.customers.CreateCustomer(
                        name, 
                        dobDate, 
                        subdistrict, 
                        phone
                );

        Long generatedId = this.service.createCustomer(customerData);

        if (generatedId > 0) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Data pelanggan berhasil disimpan!", 
                    "Sukses", 
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            this.clearForm();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Gagal menyimpan data pelanggan. Periksa log konsol untuk detail kesalahan.", 
                    "Kesalahan Sistem", 
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

